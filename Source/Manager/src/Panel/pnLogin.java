    private boolean connect(){
        try{
            LookAndFeelWindow.LookAndFeelWindow();
            Statement stManager = con.createStatement();
            password = new String(txtPassword.getPassword());
            password = password.trim();
            username = txtUsername.getText().trim();
            Pattern pUser = Pattern.compile("[a-zA-Z\\d]{3,15}");
            Matcher mUser = pUser.matcher(username);
            
            if (mUser.matches() == false) {
                check = false;
            }
            if (password.trim().length() > 15 || password.trim().length() < 3) {
                check = false;
            }
            
            ResultSet rsManager = stManager.executeQuery("select * from Manager where Username = '" + username + "' And Password = '" + password + "'");
            if (rsManager.next()) {
                tendangnhap = rsManager.getString(2);
                id_manager = rsManager.getInt(1);
                check = true;
            }
            if (rsManager.wasNull()) {
                check = false;
            }
            rsManager.close();
            stManager.close();
        }catch(Exception ex){
            ex.printStackTrace();
        }
        return check;
        
    }
